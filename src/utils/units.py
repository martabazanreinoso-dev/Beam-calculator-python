# Unit system used in the program
# Length is entered in metres
LENGTH_UNIT = "m"

# Point Loads and reactions are entered in kilonewtons
FORCE_UNIT = "kN"

# Applied moments and support moments are entered in kilinewton-
MOMENT_UNIT = "kN*m"

# Distributed Loads are entered in kilonewtons per metre
DISTRIBUTED_LOAD_UNIT = "kN/m"